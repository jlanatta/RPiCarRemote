import UIKit

extension UIColor {
    class func appRedColor() -> UIColor {
        return UIColor(red: 0.753, green: 0.224, blue: 0.169, alpha: 1)
    }

    class func appGreenColor() -> UIColor {
        return UIColor(red: 0.153, green: 0.682, blue: 0.376, alpha: 1)
    }
    
    class func appColor() -> UIColor {
        return UIColor(red: 0.173, green: 0.243, blue: 0.314, alpha: 1)
    }
}